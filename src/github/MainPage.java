    private void findActionPerformed(java.awt.event.ActionEvent evt) {                                        
        // TODO add your handling code here:
        String rname=rep.getText();
        String fname=Fname.getText();
        if(rname.equals(repss.get(repss.size()-1).getName())){
            try {
                String t=repss.get(repss.size()-1).FindFile(fname);
                if(t.equals("false")){
                      JOptionPane.showMessageDialog(rootPane, "File not found", "Error", HEIGHT);
                }else{
                    JOptionPane.showMessageDialog(rootPane, "File found at "+t, "Successful", HEIGHT);
                }
            } catch (IOException ex) {
                Logger.getLogger(MainPage.class.getName()).log(Level.SEVERE, null, ex);
            }
            
        }else{
            for(Repository r:repss){
                if(rname.equals(r.getName())){
                try {
                    String t=r.FindFile(fname);
                    if(t.equals("false")){
                          JOptionPane.showMessageDialog(rootPane, "File not found", "Error", HEIGHT);
                          
                    }else{
                        JOptionPane.showMessageDialog(rootPane, "File found at "+t, "Successful", HEIGHT);
                    }
                } catch (IOException ex) {
                    Logger.getLogger(MainPage.class.getName()).log(Level.SEVERE, null, ex);
                }
                break;
                }
            }
        }
    }
    public void retrieve() throws SQLException {
        //Connection con = null;
        //Statement stat = null;
        //ResultSet res = null;
        ResultSet rs1 = null;
        String query2 = "SELECT REPOSITORY_NAME,ADDRESS FROM SALMAN.REP WHERE USER_NAME=?";
        PreparedStatement ptmt = con.prepareStatement(query2);
        ptmt.setString(1, currUser);
        rs1 = ptmt.executeQuery();
        List<String> lis = new ArrayList<String>();
        while (rs1.next()) {
            String temp = rs1.getString("repository_name");
            String temp2 = rs1.getString("address");
            lis.add(temp);
            repss.add(new Repository(temp, temp2));
        }
    }

